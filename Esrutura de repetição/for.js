// for - laço de repetção = loop
// Um laço de repetção tem voltas - loop

// Laço contado - quando sabemos quantas voltas
// Laço condicional - irá fazer as voltas se a condição for verdadeira

// a variavel i vale 0 ou seja, essa variavel armazena voltas
// a condição verifica o numero em que o loop esta



for(let i = 0; i <= 9; i = i + 1){
console.log("A volta que o for está é: " + i);
}

for(let i = 1; i <= 10; i = i + 1){
console.log("A volta que o for está é: " + i);
}
